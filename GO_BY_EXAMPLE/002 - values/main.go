package main

import "fmt"

func main() {
	fmt.Println("VALUES: ")
	
	/* Concatenation */
	fmt.Println("Concate" + "nation")
	
	/* Integers */
	fmt.Println("1+1 =", 1+1)
	
	/* Floats */
	fmt.Println("7.0/3.0 =", 7.0/3.0)
	
	/* Booleans */
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)

}
